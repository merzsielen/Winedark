#ifndef POLYGONS_H
#define POLYGONS_H

namespace Winedark
{
	/*----------------------------------------------------------------------------------------------*/
	/* -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- */
	/* Polygons																						*/
	/* -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- -- */
	/*----------------------------------------------------------------------------------------------*/
	/*-----------------------------------------------------------------------*/
	/* Vertex																 */
	/*-----------------------------------------------------------------------*/
	struct Vertex
	{
		// Spatial Coordinates
		float x, y, z;

		// Color
		float r, g, b, a;

		// Texture Coordinates
		float s, t;
	};

	/*-----------------------------------------------------------------------*/
	/* Triangle																 */
	/*-----------------------------------------------------------------------*/
	struct Triangle
	{
		Vertex a, b, c;
	};

	/*-----------------------------------------------------------------------*/
	/* Quad																	 */
	/*-----------------------------------------------------------------------*/
	struct Quad
	{
		Triangle a, b;
	};
}

#endif